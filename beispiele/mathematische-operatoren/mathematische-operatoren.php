<?php

$zahl = 20;

# GLEICH
if ($zahl == 20) {
    echo ("Gleiche $zahl");
}

# UNGLEICH
if ($zahl != 19) {
    echo ("Ungleiche $zahl");
}

# KLEINER
if ($zahl < 22) {
    echo ("Kleinere $zahl");
}

#KLEINER-GLEICH
if ($zahl <= 22) {
    echo ("Kleinere oder gleiche $zahl");
}

#GRÖßER
if ($zahl > 19) {
    echo ("Größere $zahl");
}

#GRÖßER-GLEICH    
if ($zahl >= 19)  {
    echo ("Größere oder gleiche $zahl");
}

?>