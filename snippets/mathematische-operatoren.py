zahl = 20

# GLEICH
if zahl == 20:
    print("Gleiche Zahl")

# UNGLEICH
if zahl != 19:
    print("Ungleiche Zahl")
    
# KLEINER
if zahl < 22:
    print("Kleinere Zahl")

#KLEINER-GLEICH
if zahl <= 22:
    print("Kleinere oder gleiche Zahl")

#GRÖßER
if zahl > 19:
    print("Größere Zahl")

#GRÖßER-GLEICH    
if zahl >= 19:
    print("Größere oder gleiche Zahl")